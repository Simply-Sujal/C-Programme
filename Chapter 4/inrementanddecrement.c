
//Here we will learn about the increament and decrement 

#include <stdio.h>

int main(){
    // increament operator  
    // ++i -> pre increment operator
    // i++ -> post increment operator

    // decrement operator
    // --i -> pre decrement operator 
    // i-- -> post decrement operator
    int i = 2;
    printf("%d \n" , i++);
    printf("%d \n" , ++i);
    printf("%d \n" , ++i);

    printf("%d \n" , i--);
    printf("%d \n" , --i);
    printf("%d \n" , i);
}